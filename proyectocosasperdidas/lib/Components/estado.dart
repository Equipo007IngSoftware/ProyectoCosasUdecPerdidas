///enum de Estados
///
///perdido: El reporte pertenece a quien perdio el objeto<br />
///encontrado: El reporte fue creado por un Administrador<br />
///match: El reporte fue emparejado con otro<br />
///entregado: El reporte esta obsoleto, ya que se entrego<br />
///cancelado: El reporte esta obsoleto, ya que el usuario lo cancelo<br />
enum Estado{
  perdido,
  encontrado,
  match,
  entregado,
  cancelado;
}